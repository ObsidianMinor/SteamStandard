namespace Steam
{
    /// <summary>
    /// Specifies a game server region
    /// </summary>
    public enum Region : byte
    {
        /// <summary>
        /// The east US region code
        /// </summary>
        USEast = 0,
        /// <summary>
        /// The west US region code
        /// </summary>
        USWest = 1,
        /// <summary>
        /// The south america region code
        /// </summary>
        SouthAmerica = 2,
        /// <summary>
        /// The Europe region code
        /// </summary>
        Europe = 3,
        /// <summary>
        /// The Asia region code
        /// </summary>
        Asia = 4,
        /// <summary>
        /// The Australia region code
        /// </summary>
        Australia = 5,
        /// <summary>
        /// The middle east
        /// </summary>
        MiddleEast = 6,
        /// <summary>
        /// The Africa region code
        /// </summary>
        Africa = 7,
        /// <summary>
        /// The world region code
        /// </summary>
        World = 255,
    }
}
